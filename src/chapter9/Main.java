package chapter9;

// Inheritance is when one class becomes an extension of another class, therefore
// inheriting the members of that class

public class Main {

        private String name;
        private int age;
        private String gender;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }







}
